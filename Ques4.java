//print 1 to 10 to the moniter usingfor,while,do...while!

public class Ques4
{
	public static void main(String[]args)
	{
		for (int i=0;i<=10;i++)
		{
			System.out.println("using for "+i);
		}
		//using while
		
		int a=1;
		
		while (a<=10)
		{
			System.out.println("using while "+a);
			a++;
		}
		System.out.println(" ");
		
		//using do
		
		int b=1;
		do
		{
			System.out.println("using do "+b);
			b++;
		}
		while(b<=10);
		
		
		
		
		
		
		
		
	}
}

			